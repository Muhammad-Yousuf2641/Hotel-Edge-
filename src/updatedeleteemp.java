    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
            // TODO add your handling code here:
        
        String mobilenum=jTextField6.getText(); // for mobile number
        String name=jTextField1.getText();
        String fathername=jTextField2.getText();
        String email=jTextField4.getText();
        String permanentadd=jTextField5.getText();
        String nic=jTextField8.getText();
        
        String status=(String)jComboBox1.getSelectedItem();
        
try{
//    RETRIVING THE SQL 
            Connection con = Connectionprovider.getCon();
// Always update student record
        PreparedStatement ps = con.prepareStatement("UPDATE employee SET name=?,fathername=?,email=?,address=?,nic=?,status=? WHERE mobilenum=?");
        ps.setString(1, name);
        ps.setString(2, fathername);
        ps.setString(3, email);
        ps.setString(4, permanentadd);
        ps.setString(5, nic);
        ps.setString(6, status);
        ps.setString(7, mobilenum);
        ps.executeUpdate();

        JOptionPane.showMessageDialog(null, "Successfully Updated");
        clear();
            } 
catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

