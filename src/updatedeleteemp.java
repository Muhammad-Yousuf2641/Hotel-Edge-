    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        String mobilenum=jTextField6.getText(); // this field belongs to mobile num 
        
        try {
            Connection con=Connectionprovider.getCon();
            Statement st =con.createStatement();
            ResultSet rs =st.executeQuery("SELECT * FROM employee WHERE mobilenum='"+mobilenum+"'");
            if(rs.next())
            {
                jTextField6.setEditable(false); // this is mobilenum field 
                jTextField1.setText(rs.getString(2));   // for name 
                jTextField2.setText(rs.getString(3));   // for fathername 
                jTextField4.setText(rs.getString(4));   // email
                jTextField5.setText(rs.getString(5));   // address
                jTextField8.setText(rs.getString(6));   // nic 
                jTextField9.setText(rs.getString(7));   // designation
                jTextField9.setEditable(false);           // designation not editible
                
                if(rs.getString(8).equals("Working"))
                {
                 jComboBox1.addItem("Working");
                 jComboBox1.addItem(" Not working");
                }
                else {
                    jComboBox1.addItem("Not working");
                    jComboBox1.addItem("Working");
                }                
            }
            else{ 
                JOptionPane.showMessageDialog(null, "Employee Does not exist");
                clear();
            }
        } 
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed
